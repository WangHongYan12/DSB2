/* ------------ 串口全局中断回调 -------------*/
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
    if (huart->Instance == USART2)
    {
        MotorRx_RxISR();     /* ② 把收到的字节交给解析器 */
    }
    else if(huart->Instance == UART5) {
        LT_UART5_RxISR();
    }
    else if (huart->Instance == UART4) {
        IMU_UART4_RxISR();
    }
    else if(huart->Instance == USART3)
        Vision_RxISR();
}


